class Helper:
    def __init__(self, a, b):
        self.a = a
        self.b = b
    def add(self):
        c = self.a+self.b
        return c
    def sub(self):
        c = self.a-self.b
        return c
    def mul(self):
        c = self.a*self.b
        return c
    def div(self):
        c = self.a/self.b
        return c